foreach ($dois as $doi)
{
	
	$doi = strtolower($doi);
	
	echo "-- $doi\n";
	
	$obj = null;
	
	$url = 'https://doi.org/' . $doi;	
	$json = get($url, 'application/vnd.citationstyles.csl+json');
	$obj = json_decode($json);
	
	//print_r($obj);
	
	if (isset($obj->DOI))
	{

		$kv = array();
	
		$kv[] = array('doi', $obj->DOI);

		// author(s)
		if (isset($obj->author))
		{
			foreach ($obj->author as $author)
			{
				if (isset($author->ORCID))
				{
					$kv[] = array('author', preg_replace('/http[s]?:\/\/orcid.org\//', '', $author->ORCID));
				}
			}
		}
	
		// funder
		if (isset($obj->funder))
		{
			foreach ($obj->funder as $funder)
			{
				if (isset($funder->DOI))
				{
					$kv[] = array('funder', $funder->DOI);
				}
			}
	
		}
	
		// open?
	
		if (doi_to_oa($obj->DOI))
		{
			$kv[] = array('isAccessibleForFree', 'true');
		
			$pdfs = doi_to_pdf($obj->DOI);
			foreach ($pdfs as $pdf)
			{
				$kv[] = array('pdf', $pdf);	
			}
		}
		else
		{
			$kv[] = array('isAccessibleForFree', 'false');	
		}
	
		// kv
	
	
		// print_r($kv);
	
		foreach ($kv as $pair)
		{
			$keys = array('id', 'k', 'v');
			$values = array('"' . strtolower($obj->DOI) . '"', '"' . $pair[0] . '"', '"' . $pair[1] . '"');
	
			echo 'INSERT OR IGNORE INTO kv(' . join(",", $keys) . ') VALUES (' . join(",", $values) . ');' . "\n";
	
		}
	}
	
	
	// Give server a break every 10 items
	if (($count++ % 5) == 0)
	{
		$rand = rand(1000000, 3000000);
		echo "\n-- ...sleeping for " . round(($rand / 1000000),2) . ' seconds' . "\n\n";
		usleep($rand);
	}	
}

